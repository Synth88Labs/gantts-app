#!/usr/bin/env node
/* ============================================================
   gen-template-catalog.js — make the 100-template catalog available
   INSIDE the editor.

   The in-app picker only ever offered six hand-built templates, while
   the website had a hundred. The hundred already exist — as CSV files
   the app can import through the same path the ?csv= deep link uses —
   but the app had no list of them at runtime, because the registry
   (i18n/content.js) is a build-time file the browser never loads.

   This writes that list to js/template-catalog.js as a plain global,
   so the picker can show and search all hundred without a network
   round-trip and without a second copy of the data to keep in step.

   ONE SLUG IS ONLY LISTED IF ITS CSV ACTUALLY EXISTS. A picker entry
   that 404s on click is worse than a missing one — the deep-link funnel
   silently degraded once before (see the CSV-import history), so the
   catalog is built from what is really on disk, not from what the
   registry claims.
   ============================================================ */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const content = require(path.join(ROOT, 'i18n', 'content.js'));

/* TEMPLATE_LABELS carries only the five non-English locales — English is
   the source language and its short labels are the curated <h3> titles
   on the hand-authored hub. So English is read from templates.html and
   the other five from the registry. */
const NONEN = content.TEMPLATE_LABELS;            // { es: {slug: label}, fr: {...}, ... }
const GROUPS = content.TEMPLATE_GROUPS;

// The slug universe: the registry has all 100 in every non-English locale.
const allSlugs = Object.keys(NONEN.es);

// English labels from the hub's cards: href -> h3 text.
const hub = fs.readFileSync(path.join(ROOT, 'templates.html'), 'utf8');
const enLabel = {};
const cardRe = /href="\/templates\/([a-z0-9-]+)\.html">[\s\S]*?<h3>([^<]+)<\/h3>/g;
let m;
while ((m = cardRe.exec(hub))) enLabel[m[1]] = m[2].replace(/&amp;/g, '&').trim();
// Fallback for any slug the hub does not card: title-case the slug and
// drop the trailing type word, so it reads like a name not a filename.
const fromSlug = (s) => s
  .replace(/-(schedule|plan|timeline|template)$/, '')
  .split('-').map((w) => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');

const LABELS = { en: {}, es: NONEN.es, fr: NONEN.fr, de: NONEN.de, pt: NONEN.pt, zh: NONEN.zh };
allSlugs.forEach((s) => { LABELS.en[s] = enLabel[s] || fromSlug(s); });
const LOCALES = Object.keys(LABELS);

const filesDir = path.join(ROOT, 'templates', 'files');
const haveCsv = new Set(
  fs.readdirSync(filesDir).filter((f) => f.endsWith('.csv')).map((f) => f.slice(0, -4))
);

// group lookup: slug -> group key, for the picker's category chips
const groupOf = {};
(Array.isArray(GROUPS) ? GROUPS : Object.values(GROUPS)).forEach((g) => {
  (g.slugs || []).forEach((s) => { groupOf[s] = g.key; });
});

const missing = [];
const slugs = allSlugs.filter((s) => {
  if (haveCsv.has(s)) return true;
  missing.push(s);
  return false;
});

// labels[locale][slug] — fall back to the English label if a locale is
// somehow missing one, so the picker never renders `undefined`.
const labels = {};
for (const loc of LOCALES) {
  labels[loc] = {};
  for (const s of slugs) labels[loc][s] = LABELS[loc][s] || LABELS.en[s] || s;
}

const groups = {};
for (const s of slugs) groups[s] = groupOf[s] || 'other';

const out =
  '/* GENERATED by scripts/gen-template-catalog.js — do not edit by hand.\n' +
  '   The editor\'s template picker reads this. It lists only slugs whose\n' +
  '   CSV exists on disk, so a picker entry can never 404 on click. */\n' +
  'window.TEMPLATE_CATALOG = ' +
  JSON.stringify({ slugs, labels, groups }, null, 0) + ';\n';

fs.writeFileSync(path.join(ROOT, 'js', 'template-catalog.js'), out, 'utf8');

console.log('Template catalog');
console.log(`  ${slugs.length} template(s) with a CSV, in ${LOCALES.length} locales`);
if (missing.length) {
  console.log(`  ${missing.length} registry slug(s) had no CSV and were left out:`);
  missing.slice(0, 8).forEach((s) => console.log('      ' + s));
}
console.log('✓ js/template-catalog.js written.');
